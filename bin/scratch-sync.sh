#!/usr/bin/env bash
set -euo pipefail

# hardshell JSON → project-scratch security-status.md sync
# Usage: scratch-sync.sh <json-file> [daily|weekly]

JSON_FILE="$1"
MODE="${2:-daily}"
SCRATCH_DIR="/home/shugo/project/scratch"

if [[ ! -f "$JSON_FILE" ]]; then
  echo "ERROR: JSON file not found: $JSON_FILE" >&2
  exit 1
fi

cd "$SCRATCH_DIR" && git pull --rebase --quiet

# JSON からサマリ生成
HOSTNAME=$(jq -r '.hostname' "$JSON_FILE")
SCAN_DATE=$(jq -r '.timestamp' "$JSON_FILE")
SCANNERS=$(jq -r '.scanners_used | join(", ")' "$JSON_FILE")
CRITICAL=$(jq '.summary.critical' "$JSON_FILE")
HIGH=$(jq '.summary.high' "$JSON_FILE")
MEDIUM=$(jq '.summary.medium' "$JSON_FILE")
LOW=$(jq '.summary.low' "$JSON_FILE")
INFO=$(jq '.summary.info' "$JSON_FILE")
TOTAL=$(jq '.summary.total' "$JSON_FILE")

# EPSS/KEV トップリスク
TOP_RISKS=$(jq -r '
  [.findings[] | select(.epss_score != null or .in_cisa_kev == true)]
  | sort_by(-.risk_score) | .[0:5][]
  | "- \(.id): \(.title[:50]) (score: \(.risk_score), EPSS: \(.epss_score // "n/a"), KEV: \(.in_cisa_kev))"
' "$JSON_FILE" 2>/dev/null || echo "- (none)")

cat > "$SCRATCH_DIR/context/security-status.md" <<EOF
# Security Status (hardshell)

> Auto-generated by hardshell scan pipeline. Do not edit manually.

- **Last scan**: $SCAN_DATE ($MODE)
- **Host**: $HOSTNAME
- **Scanners**: $SCANNERS

## Summary

| Severity | Count |
|----------|-------|
| Critical | $CRITICAL |
| High     | $HIGH |
| Medium   | $MEDIUM |
| Low      | $LOW |
| Info     | $INFO |
| **Total**| **$TOTAL** |

## Top Risks (EPSS/KEV)

$TOP_RISKS

---
Full reports: \`contabo:~/hardshell/reports/\`
EOF

cd "$SCRATCH_DIR"
git add context/security-status.md
git diff --cached --quiet || git commit -m "chore: update security status ($MODE scan)

Co-Authored-By: hardshell <noreply@hardshell>" --quiet
git push --quiet

echo "[$(date)] Security status synced to project-scratch"
